@app.route('/simulate')
def simulate():
    hazards = ["motorcycle", "dog", "cow", "bicycle", "car"]
    label = random.choice(hazards)
    conf = random.uniform(0.75, 0.98)
    lat = REAL_LAT + random.uniform(-0.0002, 0.0002)
    lng = REAL_LNG + random.uniform(-0.0002, 0.0002)
    push_to_map(label, conf, lat, lng)
    return "ok"


